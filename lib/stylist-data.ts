// ─── Определение цветотипа ───────────────────────────────

function getColorType(skinTone: string | null, hairColor: string | null, gender: Gender | null): string {
  if (gender === "male") {
    if (skinTone === "porcelain" || skinTone === "fair") {
      if (hairColor === "blonde" || hairColor === "lightbrown") return "Лето"
      if (hairColor === "red") return "Весна"
      return "Зима"
    }
    if (skinTone === "olive" || skinTone === "tan") {
      if (hairColor === "blonde" || hairColor === "red") return "Весна"
      return "Осень"
    }
    return "Осень"
  }
  // Женский
  if (skinTone === "porcelain" || skinTone === "fair") {
    if (hairColor === "blonde" || hairColor === "lightbrown") return "Лето"
    if (hairColor === "red") return "Весна"
    return "Зима"
  }
  if (skinTone === "olive" || skinTone === "tan") {
    if (hairColor === "blonde" || hairColor === "red") return "Весна"
    return "Осень"
  }
  return "Осень"
}

// ─── Основная функция ────────────────────────────────────

export function buildRecommendation(form: StylistForm): Recommendation {
  const bodyId = form.bodyType ?? "average"
  const faceId = form.faceShape ?? "oval"
  const gender = form.gender ?? "female"
  const colorType = getColorType(form.skinTone, form.hairColor, gender)

  const base = gender === "male" ? maleRecs[bodyId] : femaleRecs[bodyId]
  const shoes = gender === "male" ? maleShoes[bodyId] : femaleShoes[bodyId]
  const belt = gender === "male" ? maleBelt[bodyId] : femaleBelt[bodyId]

  return {
    ...base,
    colorType,
    gender,
    glasses: glassesByFace[faceId] ?? glassesByFace.oval,
    shoes,
    belt,
  }
}

export type StylistResult = Recommendation
